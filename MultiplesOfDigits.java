import java.util.Scanner;

public class MultiplesOfDigits {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter the String");
		String s=sc.nextLine();
		int l=s.length();
		if(l%3==0){
			int g=l/3;
		for(int i=3;i<=g;){
		
			i+=3;
		}

	}

	}}
